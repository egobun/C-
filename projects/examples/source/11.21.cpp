/////////////////////////////////////////////////////////////////////

#include <cassert>
#include <functional>
#include <utility>
#include <vector>

/////////////////////////////////////////////////////////////////////

auto merge(auto && f1, auto && f2, auto && f3)
{
	return [=] < typename T > (T && x) 
	{ 
		return f3
		(
			f1(std::forward < T > (x)), 
			
			f2(std::forward < T > (x))
		);
	};
}

/////////////////////////////////////////////////////////////////////

int main()
{
	std::vector < int > vector = { 1, 2, 3, 4, 5 };

//  -----------------------------------------------------------------

	auto lambda_1 = [](auto x){ return x == 1; };

	auto lambda_2 = [](auto x){ return x == 2; };

//  -----------------------------------------------------------------

	auto lambda_3 = merge(lambda_1, lambda_2, std::logical_or <> ());

//  -----------------------------------------------------------------

	std::erase_if(vector, lambda_3);

//  -----------------------------------------------------------------

	assert(vector == std::vector < int > ({ 3, 4, 5 }));
}

