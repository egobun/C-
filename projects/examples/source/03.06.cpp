	static void test_v2()
	{
		std::cout << "Entity::test_v2\n";

	//	++m_x; // error
	}

//  -------------------------------------

//	static        auto s_x = 1; // error

	static inline auto s_y = 2; // !!!можно определить в определении класса, не принадлежит не одному из экземляров класса, является общей для всез экземпляров

