/////////////////////////////////////////////////////////////////////////////

#include <algorithm>
#include <print>
#include <random>
#include <vector>

/////////////////////////////////////////////////////////////////////////////

void show(std::vector < int > const & vector)
{
	std::print("show : vector = {{ ");

	for (auto element : vector)
	{
		std::print("{} ", element);
	}

	std::print("}}\n"); 
}

/////////////////////////////////////////////////////////////////////////////

int main()
{
	std::vector < int > vector(5, 0);

//  -------------------------------------------------------------------------

	std::uniform_int_distribution distribution(1, 5);

//  -------------------------------------------------------------------------

	std::default_random_engine engine;

//  -------------------------------------------------------------------------

	auto lambda = [&engine, &distribution](){ return distribution(engine); };

//  -------------------------------------------------------------------------

	std::ranges::generate(vector, lambda);

//  -------------------------------------------------------------------------

	show(vector);
}

