#include <print>

////////////////////////////////////////////////////////////////////////

class Entity 
{
public :

	virtual ~Entity() = default;
};

////////////////////////////////////////////////////////////////////////

class Client : public Entity {};

////////////////////////////////////////////////////////////////////////

int main()
{
	auto x = 1, & y = x;

//  --------------------------------------------------------------------
		
	Entity * entity = new Client;

//  --------------------------------------------------------------------

	std::print("main : typeid(      x) = {}\n", typeid(      x).name());

	std::print("main : typeid(      y) = {}\n", typeid(      y).name());

	std::print("main : typeid( entity) = {}\n", typeid( entity).name());

	std::print("main : typeid(*entity) = {}\n", typeid(*entity).name());

//  --------------------------------------------------------------------

	delete entity;
}

