#include <print>
#include <thread>
#include <vector>

////////////////////////////////////////////////////////////////////

void test(int x)
{
    std::print("test : x = {}\n", x);
}

////////////////////////////////////////////////////////////////////

int main()
{
    std::vector < std::jthread > jthreads;

//  ----------------------------------------------------------------

    for (auto i = 0uz; i < std::thread::hardware_concurrency(); ++i)
    {
        jthreads.emplace_back(test, i + 1);
    }
}

