//////////////////////////////////////////////////////////////////

#include <print>
#include <type_traits>

//////////////////////////////////////////////////////////////////

void test()
{ 
	std::print("test\n");
}

//////////////////////////////////////////////////////////////////

void invoke(void(*function)()) 
{ 
	std::print("invoke\n");
	
	function();
}

//////////////////////////////////////////////////////////////////

int main()
{
	static_assert(std::is_same_v < decltype( test), void   () > );

	static_assert(std::is_same_v < decltype(&test), void(*)() > );

//  --------------------------------------------------------------

	invoke(test);
}

